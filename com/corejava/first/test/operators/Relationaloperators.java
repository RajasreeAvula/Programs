package com.corejava.first.test.operators;

public class Relationaloperators {

	public static void main(String[] args) {
		int c=10,a=5;
		
		System.out.println(c+=a);
		
		System.out.println(c-=a);
		
		System.out.println(c*=a);
		
		System.out.println(c/=a);
		
		
		
		System.out.println(c%=a);
		}

	}


